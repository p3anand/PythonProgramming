# Program to find greater than, less than or equal to two numbers

n1 = int(input("Enter number 1:\n"))
n2 = int(input("Enter number 2:\n"))

if n1 > n2:
    print(f"{n1} is greater than {n2}")
elif n1 < n2:
    print(f"{n2} is greater than {n1}")
else:
    print(f"{n1} is equal to {n2}")

print("----------------------------")

# A python script to calculate square and cube of given number
